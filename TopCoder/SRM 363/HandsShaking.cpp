#define ll long long
class HandsShaking {
    public:
    ll dp[100];
    long long countPerfect(int n) {
      dp[0] = 1LL;
      dp[2] = 1LL;
      for(int i = 4; i <= n; i += 2){
        for(int j = 0; j < i; j += 2){
          ll l = j, r = i - l - 2;
          ll c = 1;
          cout << l << "::" << r << endl;
          if(r >=0)
            dp[i] += c * dp[l] * dp[r];
        }
        cout << i << ":" <<  dp[i] << endl;
      }
      return dp[n];
    }
};

