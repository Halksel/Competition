class CondorcetVoting {
    public:
    int winner(vector<string> votes) {
      vector<int> ans(votes[0].size());
      int n = votes.size(),w = votes[0].size();
      rep(i,n){
        rep(j,w){
          ans[j] += 'z' - votes[i][j];
        }
      }
      int c = 0,ret = *max_element(all(ans)),k = -1;;
      rep(i,w){
        if(ret == ans[i]){
          ++c;
          k = i;
        }
      }
      if(c == 1) return k;
      return -1;
    }
};

