const ll INF = 1e15;
vector<ll> dijkstra(vector<Edge> g[100000], int s){
  priority_queue<NODE> Q;
  Q.push({s,0});
  vector<ll> res(N,INF);
  while(Q.size()){
    NODE q= Q.top();Q.pop();
    if(res[q.pos] == INF){
      res[q.pos] = q.cost;
    }
    else{
      continue;
    }
    for(auto n : g[q.pos]){
      Q.push({n.to,q.cost+n.cost});
    }
  }
  return res;
}
vector<int> memo(100,inf),tmp;
bool dfs(int k,int n){
  if(memo[k] == 0) return true;
  if(memo[k] != inf) return false;
  memo[k] = n;
  bool f = false;
  for(auto t: g[k]){
    f |= dfs(t.to,n+1);
  }
  return f;
}

